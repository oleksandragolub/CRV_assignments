# sweep sul numero di corrispondenze N usate per stimare P (subset),
# ripetendo l’esperimento più volte per ridurre l’effetto del caso
# obiettivo: valutare quanto la stima di P sia stabile/accurata al variare di N
def ransac_sweep_num_points_repeated(
    xyz, uv,
    Ns=(8, 12, 16, 20, 25, 30, 35, 40),      # diversi valori di N (dimensione subset)
    thr_px=10.0,                             # soglia RANSAC (pixel) per definire gli inlier
    iters=5000,                              # iterazioni RANSAC
    base_seed=0,                             # seed base per riproducibilità
    repeats=5                                # ripetizioni per ogni N (stima μ e σ)
):
    xyz = np.asarray(xyz, float)
    uv  = np.asarray(uv,  float)
    n_tot = len(xyz)                         # numero totale di corrispondenze disponibili (es. 40)

    rows = []

    # per ogni valore N: si estraggono i subset casuali e si stima P (con ripetizioni)
    for N in Ns:
        nin_list, pin_list = [], []                    # #inlier e %inlier (rispetto al subset)
        r_in_list = []                                 # RMSE sugli inlier (subset)
        r_subset_list = []                             # RMSE sul subset completo di N punti
        r_40_list = []                                 # RMSE valutato su tutti i 40 punti

        for r in range(repeats):
            # seed diversi: uno per estrarre subset e uno per RANSAC
            # così si separa la casualità del campionamento subset dalla casualità interna a RANSAC
            seed_subset = base_seed + 1000*N + r
            seed_ransac = base_seed + 2000*N + r

            # selezione casuale di un subset di N corrispondenze dal totale
            rng = np.random.default_rng(seed_subset)
            idx = rng.choice(n_tot, size=N, replace=False)

            # stima robusta di P SOLO sui punti del subset
            res = dlt_ransac(
                xyz[idx], uv[idx],
                n_iters=iters,
                thr_px=thr_px,
                seed=seed_ransac
            )

            P = res["P"]
            inl = res["inliers"]      # maschera inlier riferita al subset
            nin = int(inl.sum())      # numero di inlier nel subset

            # metriche di errore
            # - RMSE_subset: quanto P spiega i punti usati per stimarlo (subset)
            # - RMSE_40: quanto P spiega TUTTI i punti (valuta capacità di generalizzare)
            # - RMSE_in: RMSE sugli inlier del subset (qualità sui punti coerenti)
            rmse_subset, _ = reprojection_rmse(P, xyz[idx], uv[idx]) # RMSE calcolato sui punti del subset
            rmse_40, _     = reprojection_rmse(P, xyz, uv)           # RMSE calcolato su tutti i punti (40)
            rmse_in = float(res["rmse_inliers"])                     # inliers del subset

            # salvataggio statistiche della ripetizione r
            nin_list.append(nin)
            pin_list.append(100.0 * nin / N)          # percentuale inlier rispetto al subset
            r_in_list.append(rmse_in)
            r_subset_list.append(float(rmse_subset))
            r_40_list.append(float(rmse_40))

        # aggregazione: media e deviazione standard (stabilità) per questo N
        rows.append({
            "N": int(N),

            "nin_mean": float(np.mean(nin_list)),
            "nin_std":  float(np.std(nin_list, ddof=1)) if repeats > 1 else 0.0,

            "inlier%_mean": float(np.mean(pin_list)),
            "inlier%_std":  float(np.std(pin_list, ddof=1)) if repeats > 1 else 0.0,

            "rmse_in_mean": float(np.mean(r_in_list)),
            "rmse_in_std":  float(np.std(r_in_list, ddof=1)) if repeats > 1 else 0.0,

            "rmse_subset_mean": float(np.mean(r_subset_list)),
            "rmse_subset_std":  float(np.std(r_subset_list, ddof=1)) if repeats > 1 else 0.0,

            "rmse_40_mean": float(np.mean(r_40_list)),
            "rmse_40_std":  float(np.std(r_40_list, ddof=1)) if repeats > 1 else 0.0,
        })

    # stampa della tabella riassuntiva per confrontare l’effetto di N
    print("\n" + "="*95)
    print(f"RANSAC sweep vs #points — {repeats} reps (thr={thr_px}, iters={iters}, base_seed={base_seed})")
    print("="*95)

    print(" N | nin μ±σ | %inl μ±σ | RMSE_in μ±σ | RMSE_sub μ±σ | RMSE_40 μ±σ")
    print("-"*95)

    for r in rows:
        print(
            f"{r['N']:2d} | "
            f"{r['nin_mean']:.1f}±{r['nin_std']:.1f} | "
            f"{r['inlier%_mean']:.1f}±{r['inlier%_std']:.1f} | "
            f"{r['rmse_in_mean']:.2f}±{r['rmse_in_std']:.2f} | "
            f"{r['rmse_subset_mean']:.2f}±{r['rmse_subset_std']:.2f} | "
            f"{r['rmse_40_mean']:.2f}±{r['rmse_40_std']:.2f}"
        )

    return rows

