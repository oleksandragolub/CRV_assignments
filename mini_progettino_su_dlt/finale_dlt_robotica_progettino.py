"""#### **Normalizzazione di Hartley (stabilità numerica per DLT)**"""

# la normalizzazione di Hartley migliora il condizionamento numerico del DLT
# centrando i punti nell’origine e scalando la distanza media a √2 (in caso di 2D) o √3 (in caso di 3D),
# rendendo la stima tramite SVD più stabile

# normalizzazione di Hartley per punti 2D
def hartley_normalize_2d(uv):
    uv = np.asarray(uv, float)                     # converte le coordinate 2D in array NumPy di tipo float
    c = uv.mean(axis=0)                            # calcolo baricentro
    uv0 = uv - c                                   # traslazione: da centro a origine
    d = np.sqrt((uv0**2).sum(axis=1))              # distanza di ogni punto dall'origine
    s = np.sqrt(2) / (d.mean() + 1e-12)            # fattore di scala per avere distanza media = sqrt(2)
    T = np.array([[s, 0, -s*c[0]],                 # matrice omogenea di trasformazione (scala + traslazione)
                  [0, s, -s*c[1]],
                  [0, 0, 1]], dtype=float)

    uv_h = np.c_[uv, np.ones(len(uv))].T           # applicazione trasformazione
    uvn_h = T @ uv_h

    uvn = (uvn_h[:2] / (uvn_h[2:3] + 1e-12)).T     # ritorno a coordinate cartesiane
    return T, uvn                                  # T è la matrice 3x3 di normalizzazione, uvn sono i punti normalizzati

# normalizzazione di Hartley per punti 3D
def hartley_normalize_3d(xyz):
    xyz = np.asarray(xyz, float)                    # converte le coordinate 3D in array NumPy di tipo float
    c = xyz.mean(axis=0)                            # calcolo baricentro
    x0 = xyz - c                                    # traslazione: da centro a origine
    d = np.sqrt((x0**2).sum(axis=1))                # distanza di ogni punto dall'origine
    s = np.sqrt(3) / (d.mean() + 1e-12)             # fattore di scala per avere distanza media = sqrt(3)
    T = np.array([[s, 0, 0, -s*c[0]],               # matrice omogenea di trasformazione (scala + traslazione)
                  [0, s, 0, -s*c[1]],
                  [0, 0, s, -s*c[2]],
                  [0, 0, 0, 1]], dtype=float)

    X_h = np.c_[xyz, np.ones(len(xyz))].T           # applicazione trasformazione
    Xn_h = T @ X_h

    Xn = (Xn_h[:3] / (Xn_h[3:4] + 1e-12)).T         # ritorno a coordinate cartesiane
    return T, Xn                                    # T è la matrice 4x4 di normalizzazione, Xn sono i punti normalizzati

"""#### **Direct Linear Transform: stima della matrice di proiezione P da corrispondenze 3D/2D**"""

# stima della matrice di proiezione P (3x4) tramite DLT a partire da corrispondenze 3D/2D
def dlt_P_from_correspondences(xyz, uv):
    xyz = np.asarray(xyz, float)      # Nx3 punti 3D nel mondo
    uv  = np.asarray(uv, float)       # Nx2 punti 2D nell'immagine

    n = xyz.shape[0]
    if n < 6:
        # P ha 12 parametri ma è definita a scala, quindi 11 gradi di libertà
        # ogni punto dà 2 equazioni (x,y), quindi servono almeno 6 punti
        raise ValueError("DLT richiede almeno 6 punti (11 gradi di libertà)!")

    # normalizzazione di Hartley
    T3, Xn = hartley_normalize_3d(xyz)   # punti 3D normalizzati + trasformazione T3
    T2, xn = hartley_normalize_2d(uv)    # punti 2D normalizzati + trasformazione T2

    # parte della costruzione della matrice A
    # ogni corrispondenza fornisce 2 equazioni lineari (una per x e una per y)
    # quindi A ha dimensione (2n x 12)
    A = []
    for i in range(n):
        X, Y, Z = Xn[i]  # punto 3D normalizzato
        x, y    = xn[i]  # punto 2D normalizzato

        # prima equazione (coordinata x)
        A.append([X, Y, Z, 1,
                  0, 0, 0, 0,
                  -x*X, -x*Y, -x*Z, -x])

        # seconda equazione (coordinata y)
        A.append([0, 0, 0, 0,
                  X, Y, Z, 1,
                  -y*X, -y*Y, -y*Z, -y])

    A = np.asarray(A, float)

    # risoluzione del sistema Ap = 0 tramite SVD
    _, _, Vt = np.linalg.svd(A)    # SVD della matrice A (avviene la decomposizione ai valori singolari di A)

    # matrice di proiezione stimata nello spazio normalizzato
    Pn = Vt[-1].reshape(3, 4)      # si prende l’ultima riga di Vt e lo si rimodella in matrice 3×4

    # la soluzione è il vettore singolare associato al valore singolare minimo (ultima riga di Vt equivalente all’ultima colonna di V)

    # denormalizzazione: ritorno al sistema originale
    P = np.linalg.inv(T2) @ Pn @ T3
    # dove @ T3 riporta i punti 3D dal sistema normalizzato a quello reale
    # e inv(T2) @ riporta i punti immagine dal sistema normalizzato ai pixel reali

    # fissaggio della scala (la matrice P è definita a fattore moltiplicativo arbitrario)
    if abs(P[-1, -1]) > 1e-12:
        # se P è soluzione, anche λP è soluzione
        # per renderla unica, si impone una convenzione: cioè si divide tutta la matrice per l’ultimo elemento
        P = P / P[-1, -1]
    else:
        # se l’ultimo elemento fosse quasi zero, allora non si può dividere per quello
        # quindi si normalizza usando la norma della matrice
        P = P / (np.linalg.norm(P) + 1e-12)

    return P     # si restituisce la matrice di proiezione finale nel sistema originale

"""#### **Decomposizione P in (K, R, X0) usando QR**"""

# P = K [R ∣ −RX0​]
# K : matrice intrinseca (focale, centro ottico, skew)
# R : matrice di rotazione (orientamento camera)
# X0 : centro della camera nel mondo

def decompose_P_QR(P):
    P = np.asarray(P, float)  # assicura che P sia un array NumPy in formato float

    # separazione P = [H3 | h]
    H3 = P[:, :3]    # estrazione della parte 3×3
    h  = P[:, 3]     # estrazione dell'ultima colonna

    # calcolo del centro camera (è il punto 3D che viene proiettato all’infinito)
    # geometricamente è la posizione della camera nel sistema mondo
    X0 = -np.linalg.inv(H3) @ h

    # si applica la fattorizzazione QR all’inversa di H3 perché la matrice 3×3 della proiezione può essere vista come prodotto tra matrice intrinseca K e rotazione R
    H3_inv = np.linalg.inv(H3)
    # lavorando su H3_inv, la QR consente di separare in modo stabile la componente di rotazione (ortogonale) da quella intrinseca (triangolare superiore)
    Q, R_upper = np.linalg.qr(H3_inv)

    # estrazione R e K
    R = Q.T                        # si ottiene R ortogonale
    K = np.linalg.inv(R_upper)     # si ottiene K triangolare superiore

    # normalizzazione di K: impone K[2,2] = 1
    K = K / (K[2,2] + 1e-12)

    # forza fx, fy positivi in modo deterministico
    for j in [0, 1]:
        if K[j, j] < 0:        # se le focali risultano negative
            K[:, j] *= -1      # si inverte il segno della colonna di K
            R[j, :] *= -1      # si inverte la riga corrispondente di R

    # una rotazione valida deve avere det(R)=+1, con il -1 si ha una riflessione e bisogna correggere invertendo segno
    if np.linalg.det(R) < 0:
        K[:, 0] *= -1
        R[0, :] *= -1

    return K, R, X0    # restituisce matrice intrinseca, rotazione e centro della camera

"""#### **Proiezione + errori di riproiezione + visualizzazione**"""

# proiettare punti 3D nel piano immagine usando la matrice P
def project_points(P, xyz):
    X = np.c_[xyz, np.ones(len(xyz))]        # aggiunge coordinata omogenea (X,Y,Z) -> (X,Y,Z,1)
    x = (P @ X.T).T                          # proiezione in coordinate omogenee: x = PX

    # normalizzazione omogenea: divide per la terza coordinata (w)
    # per tornare a coordinate cartesiane (u, v)
    x = x / (x[:, [2]] + 1e-12)              # conversione da coordinate omogenee a cartesiane

    return x[:, :2]                          # restituisce solo coordinate 2D (u,v)

# calcolo errore di riproiezione per ogni punto
def reprojection_errors(P, xyz, uv):
    uv_hat = project_points(P, xyz)          # punti riproiettati
    e = np.linalg.norm(uv_hat - uv, axis=1)  # distanza euclidea in pixel
    return e, uv_hat

# calcolo errore RMSE (media degli errori quadratici) globale
def reprojection_rmse(P, xyz, uv):
    e, _ = reprojection_errors(P, xyz, uv)
    return float(np.sqrt((e**2).mean())), project_points(P, xyz)

