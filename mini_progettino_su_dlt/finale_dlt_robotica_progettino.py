"""#### **RANSAC-DLT: stima robusta della matrice di proiezione P con outlier rejection**"""

# questa funzione combina:
# - DLT per stimare P
# - RANSAC per eliminare outlier (corrispondenze errate)

def dlt_ransac(
    xyz, uv,
    sample_size=6,               # numero minimo di corrispondenze per stimare P (DLT richiede >= 6 punti)
    n_iters=2000,                # numero di iterazioni RANSAC
    thr_px=5.0,                  # soglia errore di riproiezione (in pixel) per definire un inlier
    min_inliers=6,               # numero minimo di inlier per accettare un modello
    seed=0,                      # seed per riproducibilità
    prefer_more_inliers=True     # criterio di selezione del miglior modello
):
    xyz = np.asarray(xyz, float)
    uv  = np.asarray(uv, float)
    n = len(xyz)

    # verifica numero minimo di punti
    if n < sample_size:
        raise ValueError(f"Servono almeno {sample_size} punti, ne hai {n}.")

    rng = np.random.default_rng(seed)           # generatore casuale riproducibile
    best = None                                 # miglior modello trovato
    best_inliers = None                         # maschera dei migliori inlier

    # ciclo principale RANSAC
    for _ in range(n_iters):
        # selezione casuale di un sottoinsieme minimo
        idx = rng.choice(n, size=sample_size, replace=False)

        try:
            # stima di P tramite DLT sui punti campionati
            P = dlt_P_from_correspondences(xyz[idx], uv[idx])
        except Exception:
            # salta iterazione se il sistema è mal condizionato
            continue

        # calcolo errore di riproiezione su tutti i punti
        e, _ = reprojection_errors(P, xyz, uv)

        # classificazione inlier/outlier
        inliers = e < thr_px
        nin = int(inliers.sum())

        # scarta modello se troppo pochi inlier
        if nin < min_inliers:
            continue

        # calcolo RMSE solo sugli inlier
        rmse_in, _ = reprojection_rmse(P, xyz[inliers], uv[inliers])

        # selezione del miglior modello
        if best is None:
            best = {"P": P, "rmse_in": rmse_in, "nin": nin}
            best_inliers = inliers
        else:
            better = False

            # criterio 1: massimizza numero di inlier
            # criterio 2: minimizza RMSE
            if prefer_more_inliers:
                if nin > best["nin"]:
                    better = True
                elif nin == best["nin"] and rmse_in < best["rmse_in"]:
                    better = True
            else:
                # criterio alternativo: solo minimo errore
                if rmse_in < best["rmse_in"]:
                    better = True

            if better:
                best = {"P": P, "rmse_in": rmse_in, "nin": nin}
                best_inliers = inliers

    # se nessun modello valido trovato
    if best is None:
        raise RuntimeError("RANSAC non ha trovato un modello valido. Aumenta n_iters o thr_px.")

    # rifitting finale: ricalcolo P usando tutti gli inlier migliori
    P_refit = dlt_P_from_correspondences(xyz[best_inliers], uv[best_inliers])

    # errore finale sugli inlier
    rmse_refit, uv_hat_in = reprojection_rmse(P_refit, xyz[best_inliers], uv[best_inliers])

    # restituisce modello robusto finale
    return {
        "P": P_refit,                              # matrice di proiezione stimata
        "inliers": best_inliers,                   # maschera inlier
        "nin": int(best_inliers.sum()),            # numero inlier
        "rmse_inliers": float(rmse_refit),         # errore finale
        "uv_hat_in": uv_hat_in                     # punti riproiettati (inlier)
    }

"""#### **Esperimento A: sweep su soglia thr_px**"""

# normalizza la scala di P (P è definita a meno di un fattore moltiplicativo)
def normalize_P_scale(P):
    P = np.asarray(P, float).copy()
    if abs(P[2,3]) > 1e-12:
        P /= P[2,3]                            # se possibile impone P[2,3] = 1
    else:
        P /= (np.linalg.norm(P) + 1e-12)       # fallback se P[2,3] circa 0, si normalizza con la norma di Frobenius
    return P



# distanza tra due matrici tramite norma di Frobenius: ||A - B||_F
# utile per confrontare quanto cambia P, K, R rispetto a un riferimento
def frob(A, B):
    return float(np.linalg.norm(A - B, ord='fro'))



# sweep di RANSAC al variare della soglia thr_px:
# - per ogni soglia stima P con RANSAC-DLT
# - calcola metriche (inlier, RMSE inlier, RMSE globale)
# - decompone P -> (K, R, X0) per controllare consistenza intrinseci/rotazione
# - confronta stabilità di P,K,R rispetto a una soglia di riferimento
def ransac_sweep_thresholds(name, xyz, uv, thresholds=(2,5,10,15,20), iters=5000, seed=0):
    results, Ps, Ks, Rs = [], [], [], []

    # si esegue RANSAC per ciascuna soglia + si salvano le metriche e parametri camera
    for thr in thresholds:
        res = dlt_ransac(xyz, uv, n_iters=iters, thr_px=float(thr), seed=seed)
        P = res["P"]
        inl = res["inliers"]

        # RMSE su tutti i punti (misura quanto P spiega l'intero set, inclusi outlier)
        rmse_all, _ = reprojection_rmse(P, xyz, uv)
        # RMSE sugli inlier (misura qualità del modello sui punti ritenuti inlier)
        rmse_in = res["rmse_inliers"]

        nin = int(inl.sum())
        n = len(xyz)

        # decomposizione P in (K, R, X0) per analizzare parametri intrinseci e orientamento
        K, R, _ = decompose_P_QR(P)

        # si salvano le metriche per questa soglia
        results.append({
            "thr_px": float(thr),
            "inliers": nin,
            "inlier_%": 100.0 * nin / n,
            "rmse_inliers_px": float(rmse_in),
            "rmse_all_px": float(rmse_all),
            "detR": float(np.linalg.det(R)),   # una rotazione valida dovrebbe avere det(R) circa +1
        })

        # vengono salvate le versioni normalizzate per confronti "stabilità" (P è a scala arbitraria)
        Ps.append(normalize_P_scale(P))
        Ks.append(K / (K[2,2] + 1e-12))  # normalizza K imponendo K[2,2]=1 (scala intrinseci)
        Rs.append(R)


    # si sceglie una soglia di riferimento (di default 10 px)
    # e misura quanto le matrici cambiano rispetto a quel riferimento
    ref_idx = thresholds.index(10) if 10 in thresholds else 0
    P_ref, K_ref, R_ref = Ps[ref_idx], Ks[ref_idx], Rs[ref_idx]

    # aggiunge per ogni soglia le distanze Frobenius da P/K/R di riferimento
    for i in range(len(results)):
        results[i]["dP_to_ref_frob"] = frob(Ps[i], P_ref)
        results[i]["dK_to_ref_frob"] = frob(Ks[i], K_ref)
        results[i]["dR_to_ref_frob"] = frob(Rs[i], R_ref)

    # si stampa la tabella riassuntiva
    print("\n" + "="*80)
    print(f"{name} — RANSAC sweep su thr_px (iters={iters}, seed={seed})")
    print("Ref per stabilità: thr_px =", thresholds[ref_idx])
    print("="*80)
    header = f"{'thr':>5} | {'inl':>4} | {'%inl':>6} | {'RMSE_in':>8} | {'RMSE_all':>9} | {'dP':>8} | {'dK':>8} | {'dR':>8}"
    print(header)
    print("-"*len(header))
    for r in results:
        print(f"{r['thr_px']:5.1f} | {r['inliers']:4d} | {r['inlier_%']:6.1f} | "
              f"{r['rmse_inliers_px']:8.2f} | {r['rmse_all_px']:9.2f} | "
              f"{r['dP_to_ref_frob']:8.2f} | {r['dK_to_ref_frob']:8.2f} | {r['dR_to_ref_frob']:8.2f}")

    return results

